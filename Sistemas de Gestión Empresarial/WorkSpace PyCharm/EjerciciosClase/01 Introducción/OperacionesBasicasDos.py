primerN = float(input("Primer número: "));
segundoN = float(input("Segundo número: "));

resultadoN = primerN + segundoN;
print("El resultado es:");
print("Suma: " + str(resultadoN));
resultadoN = primerN - segundoN;
print("Resta: " + str(resultadoN));
resultadoN = primerN * segundoN;
print("Multiplicación: " + str(resultadoN));
resultadoN = primerN / segundoN;
print("División: " + str(resultadoN));
resultadoN = primerN % segundoN;
print("Resto: " + str(resultadoN));

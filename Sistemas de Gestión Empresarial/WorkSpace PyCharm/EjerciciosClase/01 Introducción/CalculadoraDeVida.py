edad = int(input("¿Cuántos años tienes?: "));

meses = edad * 12;
print("Tienes: " + str(meses) + " meses de vida.");
semanas = meses * 4;
print("Tienes: " + str(semanas) + " semanas de vida.");
dias = meses * 30;
print("Tienes: " + str(dias) + " dias de vida.");
horas = dias * 24;
print("Tienes: " + str(horas) + " horas de vida.");
minutos = horas * 60;
print("Tienes: " + str(minutos) + " minutos de vida.");
segundos = minutos * 60;
print("Tienes: " + str(segundos) + " segundos de vida.");
